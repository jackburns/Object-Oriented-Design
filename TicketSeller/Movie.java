/** Represents a movie
 * 
 * @author Jack Burns
 * @version %I% %G% */
public class Movie {
    /** Movie name */
    String name;
    /** Movie length */
    int length;

    /** Movie Constructor
     * 
     * @param name String
     * @param length int */
    Movie(String name, int length) {
        this.name = name;
        this.length = length;
    }

}
